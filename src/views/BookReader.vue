      // Ê£ÄÊü•‰∏ãËæπÁïå - Â¶ÇÊûú‰ºöË∂ÖÂá∫Â±èÂπïÂ∫ïÈÉ®ÔºåÂàôÊòæÁ§∫Âú®ÈÄâ‰∏≠ÊñáÊú¨‰∏äÊñπ
      if (y + noteInputHeight > window.innerHeight - padding) {
