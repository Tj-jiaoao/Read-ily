      let x = originalPosition.x;
      let y = originalPosition.y;
      
      // Ê£ÄÊü•Âè≥ËæπÁïå
      if (x + noteInputWidth > window.innerWidth - padding) {
        x = window.innerWidth - noteInputWidth - padding;
      }
      
      // Ê£ÄÊü•Â∑¶ËæπÁïå
